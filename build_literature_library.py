def generate_attachment_note() -> None:
    rows = []
    for item in ATTACHMENTS:
        p = item.get("library_path", "")
        rows.append(f"| {item['title']} | {link(p, p) if p else '缺失'} | {item['note']} | {item.get('sha1', '')} |")
    text = f"""
# 附件说明

| 文件 | 库内路径 | 说明 | SHA1 |
|---|---|---|---|
{chr(10).join(rows)}
"""
    write(ROOT / "附件说明.md", text)


