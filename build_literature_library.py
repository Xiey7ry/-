def safe_title(title: str) -> str:
    replacements = {
        "：": "",
        ":": "",
        "？": "",
        "?": "",
        "“": "",
        "”": "",
        "《": "",
        "》": "",
        "——": "_",
        "、": "",
        "，": "",
        ",": "",
        "；": "",
        ";": "",
        "/": "",
        "\\": "",
        "*": "",
        "|": "",
        "<": "",
        ">": "",
    }
    out = title
    for old, new in replacements.items():
        out = out.replace(old, new)
    return out[:28]


